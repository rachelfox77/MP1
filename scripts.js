

currentYear = 1978;


document.querySelector("#scrollUp").onclick = function() {
  
  gotoTop = 0;
  
  if (currentYear == 1979) gotoTop = 0;
  else if (currentYear == 1980) gotoTop = 100;
 
  
   document.querySelector("#dates").scroll({
    top: gotoTop,
    left: 0,
    behavior: 'smooth'
  });
  
}



